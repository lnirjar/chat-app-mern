export const getAvatarWithInitials = (name: string) => {
  const colors = Object.keys(AVATARS_WITH_INITIALS);
  const randomIndex = Math.floor(Math.random() * colors.length);
  const color = colors[randomIndex];

  const firstLetter = name.match(/[a-zA-Z]/)?.[0].toUpperCase() ?? "A";

  const avatar = (
    AVATARS_WITH_INITIALS as Record<string, Record<string, string>>
  )[color][firstLetter];
  return avatar;
};
